public class Height {
    public String name;
    public int height;

    public Height(String name, int height) {
        this.name = name;
        this.height = height;
    }

    public Height() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
